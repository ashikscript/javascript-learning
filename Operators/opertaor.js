// Arithmetic operators
console.log("Arithmetic operators");


let a = 20;
let b = 10;
console.log(a + " + " + b + " = " + (a+b));      //  20 + 10 = 30
console.log(a + " - " + b + " = " + (a-b));      //  20 - 10 = 10
console.log(a + " * " + b + " = " + (a*b));      //  20 * 10 = 200
console.log(a + " / " + b + " = " + (a/b));      //  20 / 10 = 2
console.log(a + " % " + b + " = " + (a % b));      //  20 % 10 = 0



// Assignment operators
console.log("Assignment operator");
let aso = 10;     // 10
console.log("a = " + aso);     // 10
aso += 5;   // 10 + 5
console.log("a += " + aso);     // 15
aso -= 5; // 15 - 5
console.log("a -= " + aso);     // 10
aso *= 5; // 10 * 5
console.log("a *= " + aso);     // 50
aso /= 5; // 50 / 5
console.log("a /= " + aso);     // 10
aso %= 4   // 10 % 4
console.log("a %= " + aso);      // 2 