//  relational operators

let m = 10, n = 15, o = 10;
// console.log(m > n);
console.log(m + " > " + n + " = " + (m > n));   // m > n == false
console.log(m + " < " + n + " = " + (m < n));   // m < n == true
// console.log(m < n);
console.log(m + " >= " + o + " = " + (m >= o));    // m >= o == true 
console.log(m + " <= " + n + " = " + (m <= n));  // m <= n == truee
console.log(m + " == " + n + " = " + (m == n));   // m == n == falsee
console.log(m + " == " + o + " = " + (m == o));   // m == o == trueee
console.log(m + " != " + n + " = " + (m != n));   // m != n == true
console.log(m + " != " + o + " = " + (m != o));    // m != o == falseeee






